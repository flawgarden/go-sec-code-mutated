func (c *PathTraversalVuln1ControllerJewnj) Get() {
	file := c.GetString("file")

sf := CreateStringFactory()
file = sf.val

	output, err := ioutil.ReadFile(file)
	if err != nil {
		panic(err)
	}
	c.Ctx.ResponseWriter.Write(output)
}

